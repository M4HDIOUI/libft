#include "libft.h"

int	ft_atoi(const char *str)
{
	size_t	i;
	size_t	s;
	size_t	r;

	s = 1;
	i = 0;
	r = 0;
	while ((str[i] >= 9 && str[i] <= 13) || (str[i] == 32))
	{
		i++;
	}
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			s *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		r *= 10;
		r += str[i] - 48;
		i++;
	}
	return (r * s);
}
